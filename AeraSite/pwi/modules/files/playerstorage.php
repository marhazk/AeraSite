    <h1>Player Storage</h1>
    <h4>You may store extra items you have at the bank. These banks can be   accessed through the banker NPCs. When you speak to a banker, you will   be able to perform several actions.</h4>
    <p>Safe service - You can access your safety box and deposit/withdraw items and/or coins.</p>
    <p>Change safe code - You can change the code used to access your safety box here.</p>
    <p>Quest - The banker offers two quests which will allow you to   increase your safety box and inventory size upon completion of the   quests.</p>
  </div>
