  </div>
  <div>
    <h5>Manufacture</h5>
    <p>Ultimate Weapon Manufacturing</p>
    <p>1. Use Mysterious Chips and coins to obtain materials from the Mysterious Merchant (131 861) in Thousand Streams.</p>
    <p>2. When you get the materials from the Mysterious Merchant, you can go to the Warsoul Spirit (552 652) in the center of Archosaur and exchange them for the final materials and molds for your ultimate weapon.</p>
    <p>3. When all the needed materials are in hand, you can go to the "Pwr" tab and manufacture the Ultimate Weapon of your choice.</p>
