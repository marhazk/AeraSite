<p><strong>What is AeraVision FrostBlood Cultivation?</strong><br />
  AeraVision FrostBlood Cultivation (known as AFB), is a new cultivation level for Aera PerfectWorld which is located at Frostcovered City, or known as Frost Walk City, and because of this new era has started, it has merged with Nirvana, Main world and Morai World.
</p>
