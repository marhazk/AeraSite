<p><strong>What is BATTLEPOWER?</strong><br />
  Battlepower (known as BP) is a ultimate point which calculate your level, cultivations, and completion quests into BPpoint. The more BPpoint 
have gain, the more legend and popular you are.</p>
<p><strong>What is the advantages of BATTLEPOWER?</strong><br />
Battlepower (BP) helps you gain extra damages while hitting mobs or enemies. Plus, when your BPpoint are higher than the others, the higher trust and bid you are.</p>
<p><strong>How to gain BATTLEPOWER?</strong><br />
By leveling your character, do cultivation from the beginning and do all the quests that NPCs offer to you.</p>
<p><strong>Any useful information about BATTLEPOWER?</strong><br />
Battlepower is the first implemention feature that developed under Perfect World : Aera (EuBo) (www.perfectworld.my). Any details, contact any available GMs or helpers for more information.</p>
