    <h1>Targeting</h1>
  </div>
  <div>
    <div></div>
    <p>1. Hide/show character panel.</p>
    <p>2. Character's face (you can rotate your face around by clicking and dragging your left mouse button).</p>
    <p>3. Displays your character level.</p>
    <p>4. Best performance toggle, this button will increase performance   in laggy areas like cities or when there are too many players around.</p>
    <p>5. Show/hide player names and icon (will remove or show the players' name above their head)</p>
    <p>6. Show/hide players outside the range limit.</p>
    <p>7. Show/hide monsters on mini-map.</p>
    <p>8. Displays the player's Hit Points (HP).</p>
    <p>9. Displays the player's Mana Points (MP).</p>
    <p>10. Displays the player's percentage of the current level.</p>
    <p>11. Displays amount left on HP Hierogram (this pane can be moved by clicking and dragging your left mouse button).</p>
    <p>12. Displays amount left on MP Hierogram (this pane can be moved by clicking and dragging your left mouse button).</p>
    <p>13. Displays time left for flight (only displays when flight acceleration is on).</p>
    <p>14. Displays class name.</p>
    <p>15. Safety Lock (allows the player to set a certain amount of time before certain functions are available after log-in).</p>
    <p>16. Displays active buffs for the character.</p>
    <p>17. Displays the character's name and amount of Chi the player has currently.</p>
    <p>18. Displays the player's current amount of sparks (each spark is a full bar of Chi which equals 100 Chi).</p>
  </div>
