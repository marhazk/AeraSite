    <h5>Targeting Info</h5>
    <div>
      <div></div>
      <p><strong>Elite Target</strong></p>
      <p>1. Show/Hide extra mob info.</p>
      <p>2. Displays current target's HP.</p>
      <p>3. Displays target's name.</p>
      <p>4. Displays additional info on target (if applicable).</p>
    </div>
    <div>
      <div></div>
      <p><strong>Normal and NPC target:</strong></p>
      <p>1. Show/Hide extra mob info.</p>
      <p>2. Displays current target's HP.</p>
      <p>3. Displays target's name.</p>
      <p>4. Displays additional info on target (NPC's will not have additional information).</p>
    </div>
    <div>
      <div></div>
      <p><strong>Player character target:</strong></p>
      <p>1. Target character's class.</p>
      <p>2. Target character's face.</p>
      <p>3. Target character's HP.</p>
      <p>4. Target character's name.</p>
      <p>5. Target character's faction (also known as guild).</p>
      <p>6. Target character's active buffs.</p>
    </div>
  </div>
  <div>
    <h5>Squad Pane</h5>
    <div></div>
    <p>Squad pane (maximized view).</p>
    <p>1. Squad leader arrow (will display on HUD and point towards the leader).</p>
    <p>2. Minimize/Maximize Squad pane.</p>
    <p>3. Squad leader tag.</p>
    <p>4. Members of the squad with HP, MP, level and class displayed.</p>
    <p>5. Squad pane (minimized view).</p>
    <p>6. Name of current squad.</p>
    <p>7. HUD icon for party leader (always points to the direction the squad leader is currently).</p>
  </div>
