  <strong>2. Usernames, Character and Guild Names.</strong><br>
  <br>
  Each   user will create a character name within the Program. Additionally,   users may form "guilds" and such users will be required to choose a name   for the guild. When you create a character name, a guild, or otherwise   create a label that can be seen by other players using the Program, you   must abide by the following guidelines as well as the rules of common   decency. If GMs of Cubinet Interactive Sdn.Bhd. finds such a label to be   offensive or improper, it may, in its sole and absolute discretion,   change the name, remove the label and corresponding chat room, and/or   suspend or terminate your use of the Program.<br>
  <br>
  In particular, you may not use any name:<br>
  <br>
  •   Belonging to another person with the intent to impersonate that person,   including without limitation a "Game Master" or any other employee or   agent of Cubinet;<br>
  • That incorporates vulgar languages or which are   otherwise offensive, defamatory, obscene, hateful racially, ethnically   or otherwise objectionable;<br>
  • Subject to the rights of any other person or entity without written authorization from that person or entity;<br>
  • That belongs to a popular culture figure, celebrity, or media personality;<br>
  • That is, contains, or is substantially similar to a trademark or service mark, whether registered or not;<br>
  • Belonging to any religious figure or deity;<br>
  • Taken from Cubinet's Game products;<br>
  • Related to drugs, sex, alcohol, or criminal activities;<br>
  • Comprised of partial or complete sentence (e.g., "Iamking", "Yousuck", etc);<br>
  • Comprised of gibberish (e.g., "Asdfsdf", "Rthnien", "Prtyuiop");<br>
  • Referring to pop culture icons or personas (e.g. " "OJSimpson", "McDonalds", "Cicakman")<br>
