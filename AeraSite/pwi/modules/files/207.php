<p><strong>What is BATTLEPOWER?</strong></p>
<p align=center><img src="images/patch/20701.jpg"/><BR />BattlePower Rating has introduced for G15 armors</p>
<p align=center><img src="images/patch/20713.jpg"/><BR />Diamond Stone (increases your attack level) same as increasing your BattlePower Attacking Rates or BPpoint</p>
<p align=center><img src="images/patch/20714.jpg"/><BR />Vajra Stone (increases your defense level) same as increasing your BattlePower Defense Rates or BPpoint</p>
<p align=left>As you seen a photo above, it works like Diamond Stone for Attacking rates and Vajra Stone for Defensive rates. BattlePower Attack Rating will increases your critical damages, physical and magic attacks, and accuracy, however BattlePower Defence Defense Rating will increases your element defenses, dodges and physical</p>
<p align=left>You also can gain your Attacking and Defensive Rates through BattlePower Point (BP) by doing and submiting any quests especially the BLUE, YELLOW, RED/ORANGE quests at any NPCs. Once you have finished submitting those quests, you BP will increases. The more BP you have gained, the more legendary and stronger you are.</p>
