    <h1>Territory War</h1>
    <h4>Perfect World International is broken up into 44 different states, or territories that can be captured by guilds.</h4>
    <p>The 47 territories are broken up into three tiers.  A majority of   the territories are Tier 3 territories, only a handful of states are   Tier 2 territories, and only two states are Tier 1 territories.</p>
    <p>What do the tiers mean?</p>
    <p>The tiers represent the significance of the territory, the amount   of gold required to declare war, and most importantly, determines the   order in which your guild must conquer them.</p>
  </div>
  <div>
    <h5>Declaring War</h5>
    <p>In order to declare war on a territory, there are certain requirements that must be met by your guild.</p>
    <p>Rules for Declaring War:</p>
    <p>1. Your guild can only attack one territory per week.</p>
    <p>2. If your guild currently does not own any territories, then you can only declare war on Tier 3 territories.</p>
    <p>3. If your guild already owns territories, then you can only   declare war on territories that are adjacent to your existing   territories.</p>
    <p>4. In order to declare war on a Tier 2 territory, you must   already own at least two Tier 3 territories and at least one of your   territories must be directly adjacent to it.</p>
    <p>5. In order to declare war on a Tier 1 territory, you must   already own at least three territories and at least one of your   territories must be directly adjacent to it.</p>
    <p>(Territories which are colored grey indicate that your guild does   not meet the war declaration requirements for that given territory.)</p>
  </div>
  <div>
    <h5>Bidding</h5>
    <p>Upon deciding which territory your guild has set it's eyes on,   you must then bid for the right to fight for said territory.  Speak with   your local Battle Coordinator between Wednesday 7:00pm and Thursday   7:00pm (Pacific Time) to place a bid.</p>
    <p>If your guild wins the Territory War, then your guild leader will   receive 90% of the bid back.  If your guild loses the Territory War,   then your bid will be lost.</p>
    <p>Regardless of whether or not a territory is already owned, a   minimum bid must be placed.  This minimum bid is different for each   tier:</p>
    <ul>
      <li><strong>Tier 3 Territory</strong></li>
      <li>500,000 coins</li>
    </ul>
    <ul>
      <li><strong>Tier 2 Territory</strong></li>
      <li>1,000,000 coins</li>
    </ul>
    <ul>
      <li><strong>Tier 1 Territory</strong></li>
      <li>2,000,000 coins</li>
    </ul>
    <p>Whichever guild has risked the most coins and bid the highest, will participate in the Territory War.</p>
  </div>
  <div>
    <h5>The Battle</h5>
    <p>Territory wars are exciting and competitive, regardless of   whether you're battling another guild for ownership of a territory, or   even battling NPCs for a neutral territory.  The following rules apply   to all Territory Wars:</p>
    <p>1.  Chi is set to zero once a user enters the battleground.</p>
    <p>2.  The embrace function is disabled while in the battleground.</p>
    <p>3.  There is no experience penalty from being killed by monsters in a Neutral Territory War.</p>
    <p>4.  Anyone who quits or leaves their guild will be instantly ejected from the battleground.</p>
    <p>5.  In order to participate in your guild's Territory War, you   must be a member of your guild for at least 100 hours prior to the war.</p>
    <ul>
      <li><strong>Neutral Territory</strong></li>
      <li>If you are fighting for a neutral territory (a territory that   is not yet owned by a guild) then the war itself is really quite simple.    Kill all the monsters in the zone before the timer runs out.</li>
    </ul>
    <ul>
      <li><strong>Foreign Territory</strong></li>
      <li>If your guild is attacking a territory that is already owned by another guild, then a true battle takes place.</li>
    </ul>
    <p>Two guilds are situated on map on opposite corners.  Each guild   has a base that can be destroyed.  There are multiple routes between   both bases.</p>
    <p>The attacking guild can build catapults and the defending guild can build different types of towers.</p>
    <p>The Territory War is over once one guild's base has been destroyed, or time has run out.</p>
