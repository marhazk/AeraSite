    <h5>Divorce</h5>
    <p>If the couple in question no longer wishes to be together and   counseling has not helped then the only other option is the dissolution   of the marriage. In order to complete this action, one of the married   individuals can go to the Eldest Matchmaker and pay 2,000,000 coins to   end the marriage. The one who confirms the divorce is the only one who   will have to pay this amount.</p>
