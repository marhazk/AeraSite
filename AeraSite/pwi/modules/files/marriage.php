    <h1>Marriage</h1>
    <h4>Within the Perfect World marriage system, users have the unique   opportunity to become married to one another. In this marriage both   users will have their significant other displayed above their own name   as the wife or husband of the other partner. </h4>
    <p>All other users who come across these two characters will know that   they are a couple.  In addition to this title, married couples will   receive a bundle of gifts in celebration of their union.</p>
    <p>To complete this operation both users must meet the following conditions:</p>
    <p>The couple must be composed of a Male and Female character.</p>
    <p>Both characters must be at least level 20 in order to be given the quest. </p>
    <p>The male character will need to open the Bridegroom Pack and the   female character will need to open the Bride Pack from the Boutique. </p>
    <p>Note: Please have at least 5 inventory slots available when opening the packages to obtain all the items.</p>
    <p>The couple must be in a squad together with no additional members.   Any other users present in the squad will disqualify you from the   marriage process.</p>
    <p>Now that both users are ready to get married, simply click on the   Eldest Matchmaker NPC located at (522 655) in Archosaur and select   "Quest Related". Each user will need to complete the "Matrimony" quest.   For males they will need to turn in the "Stamp of Eternity" and the   females will need to turn in "Silk of Eternity". These items are   provided in your associated marriage packs.</p>
    <p>Once both sides have completed the quest the current squad leader   will then proceed to finalize the union by speaking to the Eldest   Matchmaker one more time. The final part of the quest is also called   "Matrimony" so at this point all you need to do is complete it is to   have the Squad Leader accept the "I Do" option.</p>
