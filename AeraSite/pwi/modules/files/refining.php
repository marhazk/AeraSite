    <h1>Refining</h1>
    <h4>Refining equipment is a method of upgrading your equipment without   having to replace it. In every major city, The Elder has the ability to   'Refine Equipment' for you. </h4>
    <p>The higher the upgrade, the more difficult it becomes. Starting   from level 1, your success is not guaranteed.  If you fail an upgrade,   your item returns to level 0 unless you use a special item.</p>
    <p>The only required items to refine a piece of equipment are Mirage   Celestones. You will need 2 stones for weapons and 1 stone for armor. As   your equipment levels up, it will gain a specific effect depending on   the type of equipment. For example, if you have a Robe with level 1   refinement and gained a +13 to health then a level 2 refinement would   yield 13*2 = +26 HP.  For more information, refer to the Refining   Bonuses chart below.</p>
    <p>Refining specific equipment has various effects:</p>
    <p>Normal Weapons - Physical Attack Upgrade</p>
    <p>Magic Instruments - Magic Attack Upgrade</p>
    <p>Physical Attack Rings - Physical Defense Upgrade</p>
    <p>Magic Attack Rings - Magic Defense Upgrade</p>
    <p>Physical Defense Ornaments - Physical Defense Upgrade</p>
    <p>Magic Defense Ornaments - Magic Defense Upgrade</p>
    <p>Evasion Improvement Ornaments - Evasion Upgrade</p>
    <p>Robes - Maximum HP Increase</p>
    <p>Armor - Maximum HP Increase</p>
