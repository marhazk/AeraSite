    <h5>Refining Equipment</h5>
    <div></div>
    <p>Increase your success rate but will also make sure that the   equipment's refining level never loses more than 1 level of refinement   upon failure.</p>
    <p>Tienkang Stones and Tisha Stones can be purchased from the   Boutique. In addition to these stones, Dragon Orbs can also assist you   in refining.</p>
    <p>Currently there are 5 levels in the boutique shop and the rest   must be crafted from the Jewelcrafter. The level of the Orb will tell   you what levels it can affect.</p>
    <p>So for example a level 1 Dragon Orb will guarantee a 100% success rate for a level 1 refinement attempt.</p>
    <p>To start refining an item simply go to an Elder and select 'Refine   Equipment'.  Simply drag a weapon or piece of armor into the first box   available.</p>
    <p>As you can see in the screenshot, this is a weapon so it only   requires 2 Mirage Celestones. You can put special items into the   'Special Materials' slot to increase your chances of success or minimize   the damage on a failed attempt.</p>
    <p>At the bottom of the window, a message will appear to inform you of the success or failure of your upgrade.</p>
