    <h1>Pets</h1>
    <h4>Pets can range from decorative pets, to mounts, to combat pets. You can access the Pet Menu by pressing Alt+P.</h4>
    <p>Pet managers are scattered around the world in various cities. They   serve a variety of functions such as hatching pet eggs, renaming pets,   and even learning new pet skills.</p>
    <p>The following is a list of Pet manager functions and what each does:</p>
  </div>
  <div>
    <h5>Pet Skills Window</h5>
    <p>Hatch Pet Egg: Hatch a Pet or Mount into a usable form.</p>
    <p>Restore Pet/Revert Pet: After a pet is hatched and you no longer wish to have it you can revert the pet back to an egg.</p>
    <p>Rename Pet: Gives the user the ability to rename their pets.</p>
    <p>Forget Pets Skills: Every pet has a limited number of active skills   it can have at any given time. If you wish to change around those   skills simply click it select it here and your pet will unlearn that   skill leaving a slot open.  Before learning or forgetting skills, you   must summon your pet.</p>
    <p>About Pet: Knowledge Base for pet related information</p>
    <p>Tame Pet: Learn Skills related to pet management</p>
    <p>Learn Pet Skills: Every unique pet in the game has its own set of   skills. In this menu, you can teach your pet new skills. The pet you   wish to learn skills for must be summoned.</p>
  </div>
  <div>
    <h5>Pet Bag Extension Quest</h5>
    <p>To start the quest talk to any Pet Skill Trainer/Pet Manager around the world.</p>
    <p>They will ask you to gather the following:</p>
    <div>
      <div></div>
      <p><strong>Hay</strong><br>
        Quest to expand current pet bag space to 2. Must turn in 20 Hay in order to complete quest.<br>
        Note:   Hay can be found by killing Highlands Tortoises in the river east of   Broken Bridge Village, Antelope Pups near the Silver Pool, Megalith   Rhinodrake south west of Archosaur, and other select mobs around the   world.</p>
    </div>
    <div>
      <div></div>
      <p><strong>Fruit</strong><br>
        Quest to expand current pet bag space to 3. Must turn in 50 Fruit in order to complete quest.<br>
        Note:   Can be found from flying mobs such as Cloudmedling Vipion in the skies   southwest of Etherblade, Native Vipion near Heaven's Tear, Seaview   Chisler in the Shattered Jade Shore, and other select mobs around the   world.</p>
    </div>
    <div>
      <div></div>
      <p><strong>Fungus</strong><br>
        Quest to expand current pet bag space to 4. Must turn in 100 Funguses in order to complete quest.</p>
    </div>
    <div>
      <div></div>
      <p><strong>Wild Ginseng</strong><br>
        Quest to expand current pet bag space to 5. Must turn in 150 Wild Ginsengs in order to complete quest.</p>
    </div>
    <div>
      <div></div>
      <p><strong>Cage</strong><br>
        Quest to expand current bag space by 1.<br>
        Available from either a player or the boutique. Cannot be found anywhere else in the game.</p>
