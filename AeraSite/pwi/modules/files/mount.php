    <h1>Mounts</h1>
    <h4>At level 30, all races unlock the ability to fly using aerocrafts.    Winged Elves start at level 1 with the ability to fly but their wings   consume MP with use.  At level 30, all races can use the aerocrafts to   fly without any penalty. </h4>
    <p>In order for you to be able to start using the aerocrafts, you must   first go to the Elder of Archosaur and complete the quest he gives you.    For all races, the quest tells you to go to your races recluse and   speak with them.  The Recluse will have you complete a task and after   that, you are able to use your races aerocraft.  While in flight, you   can use the Accelerate command to fly faster and that also has a   separate meter that drains with use.</p>
    <p>Additional mounts can be found at the Pet trainer in any city or in the Item Mall.</p>
