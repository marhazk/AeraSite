    <h5>Upgrading your mount</h5>
    <p>In order to upgrade your flying mount you will need to travel to   Thousand Streams. This is where you will encounter the aerocraft forge   (117, 853). Simply click on this forge and it will show you all the   available flying mounts by race and type that you can upgrade. </p>
    <p>Once you have decided to update your mount you will need to access   the boutique shop and and purchase wind widgets. They can be located in   the Fly -&gt; Upgrades category. Now simply access the forge and find   your flying mount. </p>
