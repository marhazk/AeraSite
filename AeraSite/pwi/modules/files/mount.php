    <h5>Fast Flight / Accelerated Flight Mode</h5>
    <p>As you can see here there are 2 versions of Israfel with   different flying speeds. In order to obtain the fastest version you will   need to make the first one first.</p>
    <p>To create this simply unequip your mount and be sure to have wind   widgets in your inventory and place them on the required matierals   icon. Doing this will light those requirements up, indiciating you have   fulfill the requirement.  Now simply hit start and it will create your   upgraded version. </p>
    <p>Its important to remember that you must do them in order because   without the previous version your flying mount cannot be upgraded.</p>
    <p>Witin in the emote page (E Key) there is a icon labeled   Accelerate. If you use this button while in flying mode you will enter   the Fast Speed mode of your aerocraft. Here the speed would be +4.50   meters/second. The amount of time you have in this mode is based on each   indiviudal craft. This flying craft has 300/600 seconds so you can   maintain this speed for that many seconds. </p>
