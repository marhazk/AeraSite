    <h5>Chi Stones</h5>
    <p>The only way to refill this bar is to place Yiyuan stones or higher   into the item itself by simply dragging and dropping. What you must   keep in mind though is that to refill this bar it will take a very large   amount of stones so you might not want to use the accelerated mode so   hastily. </p>
    <p>Yiyuan stones and similar types can be created by decomposing items   such as weapons and armors. In order to get this ability you must learn   the manufacturering skill for the item you wish to decompose. For   example if you want to decompose weapons, you need blacksmithing, if you   want to decompose armor, you need to have tailor skill, and etc.</p>
