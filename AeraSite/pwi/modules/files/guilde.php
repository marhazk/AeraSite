<div>
  <h1>Faction </h1>
  <h4>Within the game users can get together to form a Faction. A   faction is basically a group where users join together for common goals,   companionship, economic gain, and many other reasons. </h4>
  <p>To set up a faction you will need to visit a Faction Supervisor   located in Archosaur.  Once you have established one you can access the   faction page by hitting the 'G's key on your keyboard.</p>
  <p>Below the rules and restrictions for factions:</p>
  <p>Creating a Faction: First time applicants must be at least level 20 and must be able to pay 100,000 coins.</p>
  <p>Upgrading a Faction: You can upgrade your faction's tier in order to increase the member limit of your faction.</p>
</div>
<div>
  <h5>Faction Tiers</h5>
  <p>Within your faction you have the ability to promote users to higher   positions within your faction. With each rank they are given additional   abilities like inviting and promoting. The positions are as follows   Commissioner, Executor, Marshal, Director, and Leader.</p>
  <div>
    <p><strong>Tier 1:</strong></p>
    <p>50 member limit - 100,000 coins</p>
  </div>
  <div>
    <p><strong>Tier 2:</strong></p>
    <p>100 member limit - 2,000,000 coins</p>
  </div>
  <div>
    <p><strong>Tier 3:</strong></p>
    <p>200 member limit - 10,000,000 coins</p>
  </div>
  <p>Note: Once a faction is upgraded past level 1 the faction can no longer be disbanded. So be careful when deciding to upgrade.</p>
  <p>Note: All factions have a limited number of positions. They are 12   Executioners, 4 Marshall's, 1 Director and 1 leader regardless of Tier.</p>
  <p>Note: Tier 3 factions can change their guild icon.</p>
</div>
