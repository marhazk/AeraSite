  <div>
    <div></div>
    <p>Freeze Chat Window Button - Prevents the chat window from scrolling so you can see previous text messages.</p>
    <p>Common Chat Button - Chat with everyone around your area.</p>
    <p>Squad Chat Button (!!) - Chat with all your squad members. The text will be green.</p>
    <p>Faction Chat Button (!~)- Chat with players in your faction/guild. The text will be light blue.</p>
    <p>Whisper Chat Button (/&lt;Player Name&gt;) - Send a message to another player. The text will be purple.</p>
    <p>Trade Messages Button ($) - Advertise items for sale or trade   around your area. This is similar to the Common Chat feature except the   text will be orange. Also, it requires a lvl 30+ character.</p>
    <p>Create Chatroom - Feature allows you to create a public or private chatroom.</p>
    <p>Join Chat - This feature will show all current chatrooms   available. If the chat room is locked  you will need a password to   enter.</p>
    <p>World Chat Button (/@) - Send a message to everyone in the server.   The text will be yellow. *Visit the Botique shop to gain access to this   chat feature!</p>
    <p>Game Window (System Settings) - Displayed as a green 3x3 square. Turn on/off text message options and adjust the font sizes.</p>
    <p>Chat Window Size Button - Changes the size of your chat window, up to three sizes.</p>
    <p>Chat Window Color Button - Changes the background of your chat window, up to three transparent shades.</p>
    <p>Clear Chat Window Button - Removes all the text in your chat window.</p>
    <p>Green Smiley Button - Reveals a list of friendly and fun smileys.</p>
