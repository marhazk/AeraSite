    <p>3. Place the item you want to purchase here.</p>
    <p>4. Name of Shop.</p>
    <p>5. Maximum of coins you are willing to spend on buying items.</p>
    <p>6. Resets window.</p>
    <p>7. Confirm and open shop.</p>
    <p>8. Cancel and closes shop.</p>
