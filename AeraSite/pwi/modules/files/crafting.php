<div>
  <h1>Crafting</h1>
  <h4>In Perfect World International weapons, armor, and accessories can   be created by the players.  These manufacturing skills or professions   are called Apothecary, Blacksmith, Armorsmith, and Craftsman.</h4>
  <p>Upon reaching level 5 these trade skills become available. Simply speak witd the Elder of your city and he will assist you.</p>
</div>
<div>
  <h5>Manufacturing Skills</h5>
  <div>
    <div></div>
    <p><strong>Blacksmith:</strong><br>
      Able to manufacture all kinds of weapons including blades, swords, axes, hammers, bows, and more. </p>
  </div>
  <div>
    <div></div>
    <p><strong>Tailoring:</strong><br>
      Able to manufacture all kinds of armor and defensive equipment.</p>
  </div>
  <div>
    <div></div>
    <p><strong>Jeweller:</strong><br>
      Able to manufacture all kinds of accessories including necklaces, belts and rings. </p>
  </div>
  <div>
    <div></div>
    <p><strong>Apothecary:</strong><br>
      Able to manufacture all kinds of remedies and charms.</p>
  </div>
</div>
<div>
  <h5>Manufacturing</h5>
  <p>To advance in your manufacturing skills, please visit tde   corresponding NPC of that profession. As you advance in rank you will   need to complete additional quests or aquire books.</p>
  <p>As you level up your trade skill you will need to create items of   your grade. So if you are level 2 you will need to craft level 2 items   in order to advance.</p>
  <p>Higher level skill grades require that you attain a certain level before advancing to them.</p>
</div>
