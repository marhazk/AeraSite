    <h1>Player v. Player </h1>
    <h4>Within the game several modes of battle exist.  They range from   Player vs. Environment (PVE) which includes monsters, bosses, and raids   to Player vs. Player (PVP).  In PVP players actively engage each other   in combat in order to test skills and kill one another.  When you kill   another player this is known as player killing or PKing.   Below are the   different visual character statuses a player can be in:</h4>
  </div>
  <div>
    <p><strong>Blue Names:</strong>: Player is in shield mode; they cannot attack or be attacked by any players.</p>
    <p><strong>White Names</strong>: Player is currently neutral. They may have activated PK mode but has yet to attack anyone.</p>
    <p><strong>Pink Names:</strong>: Player has definitely killed other players. In   this mode you have a chance to lose items from your inventory and your   equipment slots if you are killed while infamous, so be careful!</p>
    <p><strong>Red name:</strong>: Player has killed many other players. Be careful   of red names because you can be next. The more kills you rack up the   longer you will stay in this mode. The punishment for death while you   have a red name is far more severe than usual. Killing monsters at or   above your level will reduce the duration of being infamous.</p>
    <p>Note: There are lots of safe zones in Perfect World. You can't PK in these areas.</p>
    <p>Note: Players are forced into PK status during Territory Wars and   some special activities. There is no death penalty during these times.</p>
  </div>
