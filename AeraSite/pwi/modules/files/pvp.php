  <div>
    <h5>PvP Server</h5>
    <p>Level 1-29: Between these levels the users are not permitted to   enter PK mode. They cannot attack or be attacked by other players.</p>
    <p>Level 30 and higher: These levels are free to PK. Once users   become this level they are flagged for PVP. There is no way to turn it   off but areas like cities will still be considered safe zones and will   be PK off.</p>
  </div>
  <div>
    <h5>PvE Server</h5>
    <p>Level 1-29: Between these levels the users are not permitted to   enter PK mode. They cannot attack or be attacked by other players.</p>
    <p>Level 30 and higher: These levels are free to PK but in order to   do so the player must activate it themselves. In this way a user will   not have to PK if they do not want to unless they enter areas that   require PK like territory wars.</p>
