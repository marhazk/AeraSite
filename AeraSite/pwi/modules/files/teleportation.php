    <h1>Teleportation</h1>
    <h4>You can greatly reduce your travel time by using the waypoint   system found in each town or village. This system allows you to teleport   instantly from one place to another. You can access these waypoints by   speaking to a Teleport Master. Other ways to teleport involve the skill   Town Portal and several items from the Boutique shop.</h4>
  </div>
  <div>
    <h5>Teleportation Ways</h5>
    <p>- Explore a town or village and find the Teleport Master. Have fun exploring!</p>
    <p>- Use a Teleport Stone to travel to any previously found waypoint.</p>
    <p>- Unlock all the waypoints by purchasing a Geographic Map.</p>
  </div>
  <div>
    <h5>Teleportation Map</h5>
    <div></div>
    <p>The green dot indicates a discovered waypoint.</p>
    <p>The white line reveals possible teleport paths. You might need to   speak to 2 or more Teleport Masters until you reach the desired   location. However, you can instantly reach any waypoint by using   Teleport Incense which is found in the Boutique Shop.</p>
  </div>
  <div>
    <h5>Boutique - Teleportation</h5>
    <div>
