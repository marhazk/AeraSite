<p>Each class in Perfect World has its own special ability that makes it   unique. Some abilities may not be completely obvious or seem so special,   and some classes share abilities. 
</p>
  <h2><span id="Untamed">Untamed</span>  </h2>
  <ul>
    <li><strong><a href="?op=guide/classes/venomancer">Venomancer</a></strong> - Venomancers can tame certain monsters   throughout Perfect World and use them as battle pets. They can also   transform into foxes, which gives them certain power ups. </li>
    <li><strong><a href="?op=guide/classes/barbarian">Barbarian</a></strong> - Barbarians can transform into tigers, which gives them certain power ups. </li>
</ul>
  <p>&nbsp;</p>
  <h2><span id="Winged_Elf">Winged Elf</span>  </h2>
  <p>All Winged Elves can fly from level 1, although the starting wings only take the character as fast as the character can run. </p>
  <ul>
    <li><strong><a href="?op=guide/classes/cleric">Cleric</a></strong> - Clerics can learn skills to heal themselves and other characters, as well as revive other characters. </li>
    <li><strong><a href="?op=guide/classes/archer">Archer</a></strong> - Archers are an excellent long ranged class, relying less on MP and more upon ammunition. </li>
  </ul>
  <p>&nbsp;</p>
  <h2><span id="Human">Human</span>  </h2>
  <ul>
    <li><strong><a href="?op=guide/classes/wizard">Wizard</a></strong> - Wizards, unlike other spell-casting classes, use spells based on three elements, rather than one element. </li>
    <li><strong><a href="?op=guide/classes/blademaster">Blademaster</a></strong> - The blademaster is one of the most   important damage dealers in the game. They are the most capable of   classes due to their weapon handling, stuns, and skill that make them   quite lethal. </li>
</ul>