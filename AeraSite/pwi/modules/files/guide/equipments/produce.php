      <h2> <span id="How_to_Manufacture"> How to Manufacture </span></h2>
      <ol>
        <li>Seek the appropriate NPC (i.e. Blacksmith, Tailor, Apothecary,   and Craftsman) in any town. Smaller towns will not have all the   manufacturing NPCs. </li>
        <li>Select <strong>Manufacture Equipment</strong> or <strong>Remedy Manufacturing</strong></li>
        <li>Depending on the Production Skill level, select the equipment to be manufactured </li>
        <li>Click <strong>Start</strong> to begin manufacturing. The start button is   enabled if all the necessary materials are present. Otherwise, the start   button is disabled. </li>
      </ol>
