      <h2> <span id="Materials_and_Herbs"> Materials and Herbs </span></h2>
      <ul>
        <li><strong>Lowest Grade</strong> = level 1, can be mined or harvested by any level </li>
        <li><strong>Low Grade</strong> = level 2, can be mined or harvested by any level from level 20 </li>
        <li><strong>Mid Grade</strong> = level 3, can be mined or harvested by any level from level 40 </li>
        <li><strong>High Grade</strong> = level 4, can be mined or harvested by any level from level 60 </li>
        <li><strong>Special Grade</strong> = level 5, can be mined or harvested by   any level from level 80 (only available in Momaganon and Lothranis, so   technically only level 89+ can harvest/mine special grade materials and   herbs). </li>
      </ul>
