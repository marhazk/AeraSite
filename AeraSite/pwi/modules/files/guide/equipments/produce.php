      <h2> <span id="How_to_Increase_the_Mastery_Level"> How to Increase the Mastery Level </span></h2>
      <p>Manufacturing equipments or restorative powders increase the mastery   level. However, this only works when the current production skill level   and the level of equipments or powders are the same. </p>
      <p>For example: </p>
      <ol>
        <li>If the current Production skill level is 3, only by manufacturing level 3 equipments/powders will increase the mastery level. </li>
        <li>If the current Production skill level is 6, manufacturing level 1-5 equipments/powders <strong>will not</strong> increase the mastery level. </li>
      </ol>
