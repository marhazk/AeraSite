      <h2> <span id="Decomposing_Equipment"> Decomposing Equipment </span></h2>
      <p>Although decomposing gear is not crafting, it is directly related to   your crafting skill level.  You must have a high enough crafting skill   level for the type of equipment's grade level in order to decompose the   equipment. </p>
      <p>For more information on decomposing see <a href="http://pwi-wiki.perfectworld.com/index.php/Decomposing_Equipment" title="Decomposing Equipment">Decomposing Equipment</a>. <br />
