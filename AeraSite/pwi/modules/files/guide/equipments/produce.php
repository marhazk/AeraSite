    <h1>Crafting</h1>
    <div id="bodyContent">
      <div id="contentSub"></div>
      <p>In Perfect World International weapons, armor, and accessories can   be created by the players. These manufacturing skills or professions   are called Apothecary, Blacksmith, Tailor, and Craftsman. </p>
      <p>Upon reaching level 5 these trade skills become available. Simply speak with the Elder of your city and he will assist you. </p>
      <p><a href="http://pwi-wiki.perfectworld.com/index.php/File:Icon_Apothecary.gif"><img alt="Icon Apothecary.gif" src="http://pwi-wiki.perfectworld.com/images/a/ac/Icon_Apothecary.gif" height="30" width="30" /></a> <strong>Apothecary:</strong> Able to manufacture all kinds of remedies and charms. </p>
      <p><a href="http://pwi-wiki.perfectworld.com/index.php/File:Icon_Blacksmith.gif"><img alt="Icon Blacksmith.gif" src="http://pwi-wiki.perfectworld.com/images/0/06/Icon_Blacksmith.gif" height="30" width="30" /></a> <strong>Blacksmith:</strong> Able to manufacture all kinds of weapons including blades, swords, axes, hammers, bows, and more. </p>
      <p><a href="http://pwi-wiki.perfectworld.com/index.php/File:Icon_Jeweller.gif"><img alt="Icon Jeweller.gif" src="http://pwi-wiki.perfectworld.com/images/f/fe/Icon_Jeweller.gif" height="30" width="30" /></a> <strong>Craftsman:</strong> Able to manufacture all kinds of accessories including necklaces, belts, and rings. </p>
      <dl>
        <dd>This is crafting 'Ornaments'; for creating gems see: <a href="http://pwi-wiki.perfectworld.com/index.php/Jewel_Crafting" title="Jewel Crafting">Jewel Crafting</a></dd>
      </dl>
      <p><a href="http://pwi-wiki.perfectworld.com/index.php/File:Icon_Tailoring.gif"><img alt="Icon Tailoring.gif" src="http://pwi-wiki.perfectworld.com/images/b/b2/Icon_Tailoring.gif" height="30" width="30" /></a> <strong>Tailoring:</strong> Able to manufacture all kinds of armor and defensive equipment. </p>
      <p><a href="http://pwi-wiki.perfectworld.com/index.php/File:Runecrafting_icon.png"><img alt="Runecrafting icon.png" src="http://pwi-wiki.perfectworld.com/images/c/c2/Runecrafting_icon.png" height="32" width="32" /></a> <strong>Runecrafting:</strong> Able to manufacture weapon-enhancing runes.  see: <a href="http://pwi-wiki.perfectworld.com/index.php/Runecrafting" title="Runecrafting">Runecrafting</a></p>
      <p><strong>Manufacturing</strong></p>
      <p>To advance in your manufacturing skills, please visit the   corresponding NPC of that profession. As you advance in rank you will   need to complete additional quests or acquire books. </p>
      <p>As you level up your trade skill you will need to create items of   your grade. So if you are level 2 you will need to craft level 2 items   in order to advance. </p>
      <p>Higher level skill grades require that you attain a certain level before advancing to them. </p>
