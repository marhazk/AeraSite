    <h1>Decomposing Equipment</h1>
    <div id="bodyContent">
      <div id="contentSub"></div>
      <p>Decomposing requires the proper level crafting skill in order to decompose different grade levels of equipment.<br />
        If, for example, you had a level 3 Tailor skill, you would not be able to decompose grade 11 Tailor equipment.<br />
        See Also: <a href="http://pwi-wiki.perfectworld.com/index.php/Crafting" title="Crafting">Crafting</a></p>
      <p><strong> Q. Why would I want to decompose equipment instead of just selling it for coin? </strong></p>
      <p>When you decompose equipment it will make different Chi stones   according to the grade level of the equipment. It will also different   pieces of equipment will yield different amounts of stones when   decomposed.<br />
        (Below is a chart of the Chi Stones and there point values.) </p>
      <p>Chi stone are used: </p>
      <ul>
        <li> to feed your genie to increase its stamina (see: <a href="http://pwi-wiki.perfectworld.com/index.php/Genie_Guides" title="Genie Guides">Genie Guides</a> - Frequently Asked Questions). </li>
        <li> to replenish the swift speed on your aerogear (see: <a href="http://pwi-wiki.perfectworld.com/index.php/Flying" title="Flying">Flying</a> - Temporary Speed Boosts). </li>
        <li> and to craft certain items (see: <a href="http://pwi-wiki.perfectworld.com/index.php/Crafting" title="Crafting">Crafting</a> - Apothecary - Level 6). </li>
      </ul>
      <p><strong> To decompose a piece of equipment </strong></p>
      <ol>
        <li> you must bring the equipment to the NPC of that equipment type;   armor to a Tailor, weapons to a Blacksmith, and ornaments to a   Craftsman. </li>
        <li> Click on the NPC and choose &quot;Decompose Equipment&quot; from the menu. </li>
        <li> Right click on the item or drag the item to the &quot;Decompose   Material&quot; box. Once the item is in the box the &quot;Decompose into&quot; box   shows what stone you will get after it is decomposed. </li>
      </ol>
      <ul>
        <li> At this point several thing can happen: </li>
      </ul>
      <ol>
        <li> The Start button is active and there is an amount in the coin   field to the right of the &quot;Decompose Material&quot; box - This indicates you   will be charged a fee to decompose this item. You might also notice that   instead of Chi Stones this item decomposes to Mirage Celestones. In   that case it might be better to just sell it to the NPC instead of   decomposing it, unless you think it is worth paying the fee to get the   mirages. </li>
        <li> The Start button is NOT active - this indicates that the item can not be decomposed because:
          <ol>
            <li> the item is the wrong craft type for this NPC. </li>
            <li> the Durability of the item is not 100% - in this case you will have to repair it first. </li>
            <li> it is a quest item or it is an item that can not be decomposed. </li>
          </ol>
        </li>
        <li> The Start button is active and it looks ready to decompose,   click the start button and you will get the amount of Chi Stones shown. </li>
      </ol>
